package com.sg.foundations.variables;


public class InABucket {
    
    public static void main(String[] args) {
        
        // Declaring variables
        String walrus;
        double piesEaten;
        float weightOfTeacupPig;
        int grainsOfSand;
        
        // Initialising variables with values
        walrus = "Sir Leroy Jenkis III";
        piesEaten = 42.1;
        weightOfTeacupPig = 55;
        grainsOfSand = 1000;
        
        System.out.println("Meet my pet walrus, " + walrus);
        System.out.print("He was a bit hungry today, and ate this many pies :");
        System.out.println(piesEaten);
        System.out.print("He is a bit heavy, weighing " + weightOfTeacupPig + " kilograms");
        System.out.println("after eating " + grainsOfSand + " grains of sand.");
        
    }
    
}
